<p>
    La durée d'amortissement est de 15 ans pour les bâtiments et 7 ans pour 
    les autres aménagements et les animaux lorsque c'est une exploitation 
    qui augmente son nombre de porcs. S'il n'y a pas augmentation du nombre 
    de porcs, le cheptel de départ n'influence pas les annuités. 
</p>

<p>
    Dans le cas de la production sous label biologique, les primes ne sont 
    pas prises en compte car elles sont liées &agrave; la surface agricole 
    exploitée qui le serait de toute mani&egrave;re sans l'élevage de porcs. 
</p>

<p>
    <strong>Renouvellement cheptel </strong> est une valeur tenant compte de 
    l'achat des nouvelles truies moins les réformes ainsi que l'achat des 
    porcelets pour l'engraissement. 
</p>
<p>
    Les <strong>frais vétérinaires </strong> est un forfait comprenant les vaccins, les vermifuges et les prestations vétérinaires 
</p>
<p>
    Les estimations seront réalisées par un projet sur litière de paille.
</p>
    La quantité de <strong>paille </strong> nécessaire dépend du type de logement choisi. 
</p>
<p>
    La consommation <strong>d'eau </strong> est fonction du nombre d'animaux 
    et de la composition du cheptel. Le gaspillage peut fortement influencer 
    les valeurs obtenues en élevage. 
</p>
<p>
    Pour <strong>l'électricité </strong>, la consommation varie selon le 
    mode de logement. Les maternités en cabanes ne sont pas chauffées, 
    alors qu'en bâtiment, elles le sont. 
</p>
<p>
    Les <strong>frais de certification </strong> sont pris en compte uniquement 
    pour la production bio. Dans les autres cas, ils ne sont pas comptabilisés 
    car ils peuvent &ecirc;tre remboursés par la Wallonie dans le cadre des 
    fili&egrave;res de qualité différenciée. 
</p>
<p>
    Les <strong>frais sanitaires </strong> reprennent les cotisations 
    payées dans le cadre du fond sanitaire. 
</p>
<p>
    Le poste <strong>matériels roulants </strong>&nbsp; prend en compte 
    la durée d'utilisation et du coût horaire de chaque machine. Le coût 
    horaire est basé sur des simulations faites &agrave; l'aide du site 
    internet 
    <a href='http://mecacost.cra.wallonie.be' target='_blank'>mecacost.cra.wallonie.be</a>. 
    L'utilisateur est libre d'y calculer ses propres estimations sur base de 
    son matériel et d'en adapter les valeurs. 
</p>
<p>
    La rubrique <strong>Divers </strong> est un montant forfaitaire par place 
    d'engraissement ou de truie par an pour couvrir l'achat de petits matériels 
    comme des pinces, seringues et aiguilles, anneaux, boucles auriculaires, 
    le tatouage, … 
</p>
<p>
    La simulation n'int&egrave;gre pas un éventuel salariat.
</p>